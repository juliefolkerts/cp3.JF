

public class Product {
    public String name;
    public boolean vege;

    public void setName(String name) {
        this.name = name;
    }

    public void setVege(boolean vege) {
        this.vege = vege;
    }

    public String getName() {
        return name;
    }

    public boolean isVege() {
        return vege;
    }


}

